import { mkdir, readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(fileURLToPath(new URL("..", import.meta.url)));
const docsDir = resolve(root, "docs/custom");
const outPath = resolve(docsDir, "custom-rule-status.md");
const fixturePath = resolve(root, "packages/calc-core/__tests__/fixtures/top20-priority-cases.json");
const ruleVersionPath = resolve(root, "versions/rule-version.json");

await mkdir(docsDir, { recursive: true });

const [fixtureRaw, ruleVersionRaw] = await Promise.all([
  readFile(fixturePath, "utf8"),
  readFile(ruleVersionPath, "utf8"),
]);

const top20Cases = JSON.parse(fixtureRaw);
const ruleVersion = JSON.parse(ruleVersionRaw);

const readyCount = top20Cases.filter((item) => item.status === "ready").length;
const pendingCount = top20Cases.filter((item) => item.status !== "ready").length;
const skillRuleMap = {
  "skcom_magic_rage[1]": ["phase1.lava.s1.schedule_parity"],
  skchr_chen_2: ["phase1.chen2.instant_multi_hit"],
  skchr_gdglow_3: ["phase1.gdglow3.multi_hit"],
  skchr_horn_3: ["phase1.horn3.conditional_overload"],
  skchr_texas2_3: ["phase1.texas2.appear_extra_stream"],
  skchr_chen2_3: ["phase1.chen2s3.holiday_trigger_window"],
  skchr_mizuki_2: ["phase1.mizuki2.attack_interval_shift"],
  skchr_thorns_3: ["phase1.thorns3.stable_stage"],
  skchr_ifrit_2: ["phase1.ifrit2.burn_resistance_semantic"],
  skchr_cerber_2: ["phase1.cerber2.hotblade_aspd_proxy"],
  skchr_mlynar_3: ["phase1.mlynar3.verdict_true_stream"],
  skchr_ghost2_3: ["phase1.ghost23.burst_threshold_proxy"],
  skchr_ironmn_2: ["phase1.ironmn2.iter_amp_proxy"],
  skchr_qiubai_3: ["phase1.qiubai3.switch_magical_burst"],
  skchr_logos_3: ["phase1.logos3.arts_burst_variant"],
  skchr_reed2_3: ["phase1.reed23.burn_echo_proxy"],
};

const skillRows = top20Cases
  .map((item) => {
    const status = item.status === "ready" ? "ready" : "pending";
    const ruleHints = skillRuleMap[item.skillId] ?? [];
    if (!ruleHints.length) ruleHints.push("-");
    return `| ${item.id} | ${item.operator} | ${item.skill} | ${item.operatorId ?? "-"} | ${item.skillId ?? "-"} | ${status} | ${ruleHints.join(", ")} |`;
  })
  .join("\n");

const content = `# Custom Rule Status

- Source: internal calc-core rule inventory
- Current phase: MVP-DPS first batch
- Last generated: ${new Date().toISOString()}
- Rule version: ${ruleVersion.ruleVersionId}
- Top20 progress: ready=${readyCount}, pending=${pendingCount}

## Planned first-batch categories

1. Damage type switch (CHANGE_attackType)
2. Defense shred / resistance shred
3. Extra damage stream (OTHER_*)
4. Duration/attack-count special handling

## Skill-level rule coverage status

| Case | Operator | Skill | operatorId | skillId | Status | Rule linkage |
|---|---|---|---|---|---|---|
${skillRows}

## Notes

This file is generated by \`scripts/generate-custom-docs.mjs\`.
Top20 progress source of truth: \`packages/calc-core/__tests__/fixtures/top20-priority-cases.json\`.
`;

await writeFile(outPath, content, "utf8");
console.log(`Generated ${outPath}`);
